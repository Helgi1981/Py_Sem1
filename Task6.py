# Решение 3 (принятое автотестом):

n = 385916

# Преобразуем номер билета в строку для удобного разбиения на части
ticket_str = str(n)

# Проверяем, что номер билета шестизначный
if len(ticket_str) != 6:
    print("no")
else:
    # Извлекаем первые и последние три цифры
    first_half = ticket_str[:3]
    second_half = ticket_str[3:]

    # Преобразуем части в списки целых чисел и считаем суммы
    sum_first_half = sum(int(digit) for digit in first_half)
    sum_second_half = sum(int(digit) for digit in second_half)

    # Сравниваем суммы и выводим результат
    if sum_first_half == sum_second_half:
        print("Yes")
    else:
        print("No")

